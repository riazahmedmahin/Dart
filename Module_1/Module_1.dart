void main() {

  const int a = 10;
  const int b = 4;

  // Addition
  int sum = a + b;
  print("Addition: $a + $b = $sum");

  // Subtraction
  int Subtraction = a - b;
  print("Subtraction: $a - $b = $Subtraction");

  // Multiplication
  int Multiplication = a * b;
  print("Multiplication: $a * $b = $Multiplication");

  // Division
  double Division = a / b;
  print("Division: $a / $b = $Division");

  // Modulus
  int Modulus = a % b;
  print("Modulus: $a % $b = $Modulus");

}
